package org.example.interfaces;

/**
 * L'interface FlyingCreature est destinée à être implémentée par des créatures capables de voler.
 * Elle contient une méthode représentant l'action de voler.
 */
public interface FlyingCreature {

    /**
     * Méthode représentant l'action de voler.
     *
     * @return Une chaîne de caractères décrivant le mouvement de vol de la créature.
     */
    String fly();
}
