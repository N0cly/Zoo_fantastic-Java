package org.example.interfaces;

/**
 * L'interface AquaticCreature est destinée à être implémentée par des créatures aquatiques.
 * Elle contient une méthode pour représenter la capacité de nager.
 */
public interface AquaticCreature {

    /**
     * Méthode représentant l'action de nager.
     *
     * @return Une chaîne de caractères décrivant le mouvement de nage de la créature aquatique.
     */
    String swim();
}
